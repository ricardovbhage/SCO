    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try {
            UsuarioModel usuario = new UsuarioModel();
            usuario.setNome(jTextField1.getText());
            usuario.setCPF(jFormattedTextField2.getText());
            usuario.setCEP(jTextField3.getText());
            usuario.setEndereco(jTextField4.getText());
            usuario.setBairro(jTextField5.getText());
            usuario.setCidade(jTextField6.getText());
            usuario.setEstado(jTextField7.getText());
            usuario.setTelefone(jFormattedTextField1.getText());
            usuario.setEmail(jTextField9.getText());
            usuario.setSenha(new String(jPasswordField2.getPassword()).trim());

            String senha = new String(jPasswordField2.getPassword()).trim();
            String confirmacao = new String(jPasswordField1.getPassword()).trim();

            if (!senha.equals(confirmacao)) {
                JOptionPane.showMessageDialog(null, "A senha e a confirmação devem ser iguais ");
            } else {

                _serviceUsuario.inserir(usuario);
                CarregarUsuarios();
                LimparFormulario();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao inserir o usuário: " + e.getMessage());
        }

    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        try {
            if (jTextField10.getText().trim().isEmpty()){
                CarregarUsuarios();
            }else{
            int id = Integer.parseInt(jTextField10.getText());
            DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
            modelo.setNumRows(0);
            jTable2.getColumnModel().getColumn(0)
                    .setPreferredWidth(10);
            jTable2.getColumnModel().getColumn(1)
                    .setPreferredWidth(120);
            jTable2.getColumnModel().getColumn(2)
                    .setPreferredWidth(80);
            jTable2.getColumnModel().getColumn(3)
                    .setPreferredWidth(80);
            jTable2.getColumnModel().getColumn(4)
                    .setPreferredWidth(120);

            UsuarioDao dao = new UsuarioDao();
            UsuarioModel c = dao.getUsuarioById(id);
            
           modelo.addRow(new Object[]{c.getId(), c.getNome(), c.getCPF(),c.getTelefone(), c.getEmail()});
         }
            
        } catch (Exception e) {

        }
      // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        int indiceSelecionado = jTable2.getSelectedRow();
        UsuarioModel item = usuariosList.get(indiceSelecionado);
        int retorno = JOptionPane.showConfirmDialog(null, "Confirma a Exclusão de : " + item.getNome());
        if (retorno == 0) {
            _serviceUsuario.remover(item.getId());
            CarregarUsuarios();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

