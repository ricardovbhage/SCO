    private void CarregarUsuarios()
    {
        try {
            DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
            modelo.setNumRows(0);
            jTable2.getColumnModel().getColumn(0)
                    .setPreferredWidth(10);
            jTable2.getColumnModel().getColumn(1)
                    .setPreferredWidth(120);
            jTable2.getColumnModel().getColumn(2)
                    .setPreferredWidth(80);
            jTable2.getColumnModel().getColumn(3)
                    .setPreferredWidth(80);
            jTable2.getColumnModel().getColumn(4)
                    .setPreferredWidth(120);

            UsuarioDao dao = new UsuarioDao();
            usuariosList = dao.getUsuarios();
            for (UsuarioModel c : usuariosList) {
                modelo.addRow(new Object[]{c.getId(), c.getNome(), c.getCPF(),
                    c.getTelefone(), c.getEmail()});
            }
        } catch (Exception e) {

        }
    }

